package Objetos;

/**
 *
 * @author alonso
 */
public class Usuario {
    
    private String nomUsuario;
    private String apelUsuario;
    private String correoUsuario;
    private String conUsuario;
    private String idUsuario;
    
    
    

  

   

    public Usuario() {
    }

    public String getNomUsuario() {
        return nomUsuario;
    }

    public void setNomUsuario(String nomUsuario) {
        this.nomUsuario = nomUsuario;
    }

    public String getApelUsuario() {
        return apelUsuario;
    }

    public void setApelUsuario(String apelUsuario) {
        this.apelUsuario = apelUsuario;
    }

    public String getCorreoUsuario() {
        return correoUsuario;
    }

    public void setCorreoUsuario(String correoUsuario) {
        this.correoUsuario = correoUsuario;
    }

    public String getConUsuario() {
        return conUsuario;
    }

    public void setConUsuario(String conUsuario) {
        this.conUsuario = conUsuario;
    }

    public String getIdUsuario() {
        return idUsuario;
    }

    public void setIdUsuario(String idUsuario) {
        this.idUsuario = idUsuario;
    }

    
    
    
}
